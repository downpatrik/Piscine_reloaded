#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		i;

	str = (char*)malloc(sizeof(*src + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
